    override fun compress(sample: Short): Int {
        var value = sample
        val sign: Int
        val exponent: Int
        val mantissa: Int
        val compressedByte: Int
        sign = value.toInt() shr 8 and 0x80
        if (sign != 0) {
            value = (value * -1).toShort()
        }
        if (value > cClip) {
            value = cClip.toShort()
        }
        value = (value + cBias).toShort()
        exponent = uLawCompressTable[value.toInt() shr 7 and 0x00FF]
        mantissa = value.toInt() shr exponent + 3 and 0x0F
        compressedByte = (sign or (exponent shl 4) or mantissa).inv()
        return compressedByte and 0x000000FF
    }

    companion object {
        const val cClip = 32635
        const val cBias = 0x84
    }
}